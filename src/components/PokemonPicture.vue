<template lang="">
    <div>
      <div class="pokemon-container">
       <img class='hidden-pokemon' :src="imgSrc"  alt="pokemon">
       <img :src="imgSrc" class='fade-in' v-if="showPokemon" alt="pokemon">
      </div>
    </div>
</template>
<script>
export default {
  props: {
    pokemonId: {
      type: Number,
      required: true,
    },
    showPokemon: {
      type: Boolean,
      required: true,
      default: false,
    },
  },

  name: "PokemonPicture",
  computed: {
    imgSrc() {
      return `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/other/dream-world/${this.pokemonId}.svg`;
    },
  },
};
</script>

